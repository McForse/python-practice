# Задача 2.2. Реализовать функцию-транскодер из формата
def f22(x):
    a = (x & 0x1ff) << 4
    b = (x & 0x7fe00) << 4
    c = (x & 0x80000) << 12
    d = (x & 0x3f00000) << 3
    e = (x & 0x3C000000) >> 26
    f = (x & 0x40000000) >> 1
    g = (x & 0x80000000) >> 1
    return a + b + c + d + e + f + g


print(f22(0x201dc7ce))
print(f22(0x02c916b3))

